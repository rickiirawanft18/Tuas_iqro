package com.gamewae.iqra.entity;

public class Tutor {
	private String hijaiyah;
	private int img;
	private String title;
	private String text;
	private int sound;
	
	public String getHijaiyah() {
		return hijaiyah;
	}
	public void setHijaiyah(String hijaiyah) {
		this.hijaiyah = hijaiyah;
	}
	public int getImg() {
		return img;
	}
	public void setImg(int img) {
		this.img = img;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public int getSound() {
		return sound;
	}
	public void setSound(int sound) {
		this.sound = sound;
	}
	
}
